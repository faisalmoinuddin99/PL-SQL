-- IF statements

/*
These are used to decide whether to allow or skip a sequence of statements.
They have the following three forms:
1. IF THEN
2. IF THEN ELSE
3. IF THEN ELSIF


The syntax to be used for IF statements are as follows:

1. IF THEN

IF condition THEN
 statements;
END IF;

2. IF THEN ELSE

IF condition THEN
 statements;
ELSE
 else_statements;
END IF;

3. IF THEN ELSIF

IF condition_1 THEN
 statements_1
ELSIF condition_2 THEN
 statements_2
[ ELSE
 else_statements
]
END IF;
*/